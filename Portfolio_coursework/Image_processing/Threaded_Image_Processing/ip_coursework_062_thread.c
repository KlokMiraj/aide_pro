#define widthx 100 
#define heightx 72
#define n 4
#define total_pixel widthx*heightx

//struct for thread strides
typedef struct {
int start_index;
int stride;
} t_args;

//inbuilt structure for time calculation
struct timespec start, finish;

// variable to hold time difference
long long int time_el;


unsigned char image[], results[widthx * heightx];

//since 4 threads will be used, 4 different thread_Argument  variables are required
t_args targ_array[n];

int time_difference(struct timespec *start, struct timespec *finish, 
                    long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec; 
  long long int dn =  finish->tv_nsec - start->tv_nsec; 

  if(dn < 0 ) {
    ds--;
    dn += 1000000000; 
  } 
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}


//thread function that will be called by 4 threads which will detect edges 
void *detect_edge_thread_function(void *ptr1){
	
    t_args *ptr=ptr1;	
    for(int i=ptr->start_index;i<total_pixel;i=i+ptr->stride){
		
		int x, y; // the pixel of interest
    int b, d, f, h; // the pixels adjacent to x,y used for the calculation
    int r; // the result of calculate
    
    y = i / widthx;
    x = i - (widthx * y);

    if (x == 0 || y == 0 || x == widthx - 1 || y == heightx - 1) {
      results[i] = 0;
    } else {
      b = i + widthx;
      d = i - 1;
      f = i + 1;
      h = i - widthx;

      r = (image[i] * 4) + (image[b] * -1) + (image[d] * -1) + (image[f] * -1)
          + (image[h] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        results[i] = 255;
      } else {
        results[i] = 0;
      }
    }
		
		
	}
	//ensuring that actualy 4 threads have been created by the program
	printf("Thread ID:  %ld\n",pthread_self());

   pthread_exit(NULL);
	
}


void detect_edges() {

      pthread_t thread_array[n];
     
	//reading of time right before starting the edge detecting process begins
      clock_gettime(CLOCK_MONOTONIC, &start);
      
	//loop for initializing the thread_Argument struct variables and creating 4 threads
      for(int i=0;i<n;i++){
        targ_array[i].start_index=i;
	targ_array[i].stride=4;
	pthread_create(&thread_array[i], NULL, detect_edge_thread_function, &targ_array[i]);
	}

	//waiting for 4 different threads to finish their job and get joined back to the main thread       
      for(int i=0;i<n;i++){
       pthread_join(thread_array[i], NULL); 
      }

     //reading of time right after finishing the edge detecting process
     clock_gettime(CLOCK_MONOTONIC, &finish);

     //calculating time difference
     time_difference(&start, &finish, &time_el);
     printf("Time elapsed was %lldns or %0.9lfs\n", time_el, 
		 (time_el/1.0e9)); 


}


//parameters for the display window using free glute library called from main function
static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(widthx, heightx, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(widthx, heightx, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}

//function to terminate the execution when the user presses the key in the interuption handling function
void tidy_and_exit() {
  exit(0);
}

//Handling key press
void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

//handling user interuption
static void key_pressed(unsigned char key, int x, int y) {
  switch(key){
    case 27: // escape
      tidy_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}

//main function
int main(int argc, char **argv) {
  signal(SIGINT, sigint_callback);
 
  printf("image dimensions %dx%d\n", widthx, heightx);
  detect_edges();

  glutInit(&argc, argv);
  glutInitWindowSize(widthx * 2,heightx);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
      
  glutCreateWindow("6CS005 Image Progessing Courework Multithreaded version");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0); 

  glutMainLoop(); 

  tidy_and_exit();
  
  return 0;
}

